from __future__ import absolute_import, division
from __future__ import print_function, unicode_literals


def add(a, b):
    """
    Adds ``a`` and ``b`` and returns the result.

    See also :meth:`subtract()`.
    """
    return a + b


def subtract(a, b):
    """
    Subtracts ``b`` from ``a`` and returns the result.

    See also :meth:`add()`.
    """
    return a - b

